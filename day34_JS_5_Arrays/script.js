// shift and pop is brother
// unshift and push is brother

let arr = [10, 20, 30, 40];

// push() push element in last
arr.push(30)
console.log(arr)

// pop() pop element in last
arr.pop()
console.log(arr)

// shift() pop element in first
arr.shift()
console.log(arr)

// unshift() push element in first
arr.unshift(10)
console.log(arr)

